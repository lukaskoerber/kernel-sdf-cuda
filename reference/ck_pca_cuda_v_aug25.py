def transform_data(characteristics, c, include_constant=False):
    # Step 1: Original Features Squared
    squared_features = characteristics ** 2
    squared_features.columns = [f'{col}^2' for col in characteristics.columns]

    # Step 2: Pairwise Interaction Terms
    X = characteristics.values
    n_samples, n_features = X.shape
    col_names = characteristics.columns

    # Get upper triangle indices for combinations (i < j)
    idx_upper = np.triu_indices(n_features, k=1)
    # Compute interactions using numpy's advanced indexing and broadcasting
    interactions = np.sqrt(2) * X[:, idx_upper[0]] * X[:, idx_upper[1]]

    # Create column names for interaction terms
    interaction_col_names = [
        f'{col_names[i]}_{col_names[j]}' for i, j in zip(idx_upper[0], idx_upper[1])
    ]

    # Create DataFrame for interaction terms
    interaction_terms = pd.DataFrame(
        interactions, columns=interaction_col_names, index=characteristics.index
    )

    # Step 3: Include Original Features Scaled by sqrt(2c)
    linear_terms = np.sqrt(2 * c) * characteristics

    if include_constant:
        # Step 4: Add constant term
        constant_term = pd.DataFrame(
            c, index=characteristics.index, columns=['constant']
        )
        return pd.concat(
            [squared_features, interaction_terms, linear_terms, constant_term], axis=1
        )

    return pd.concat(
        [squared_features, interaction_terms, linear_terms], axis=1
    )


