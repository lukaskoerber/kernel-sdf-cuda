def compute_characteristic_signals(characteristics: pd.DataFrame) -> pd.DataFrame:
    """
    Compute characteristic signals for every date in the given characteristics DataFrame, adjusted for leverage.

    Parameters:
    characteristics (pd.DataFrame): DataFrame containing characteristics with a MultiIndex (date, other levels).

    Returns:
    pd.DataFrame: DataFrame with computed characteristic signals for each date, adjusted for leverage.
    """
    def leverage_adjust(signal):
        leverage_factor = 1 / signal[signal > 0].sum()
        return signal * leverage_factor

    # Rank the characteristics
    characteristics_ranked = characteristics.groupby(level='date').rank(method='average') / (characteristics.groupby(level='date').transform('count') + 1)

    # Compute the mean of the ranked characteristics
    characteristics_mean = characteristics_ranked.groupby(level='date').transform('mean')

    # Compute the signal
    characteristics_signal = characteristics_ranked - characteristics_mean

    # Normalize the signal
    characteristics_signal = characteristics_signal.groupby(level='date').transform(lambda x: x / x.abs().mean())

    # Adjust for leverage
    characteristics_signal = characteristics_signal.groupby(level='date').transform(leverage_adjust)

    # divide all signals by 2
    characteristics_signal = characteristics_signal / 2

    return characteristics_signal


