def drop_low_coverage_securities(characteristics, max_missing):
    """Filter out (permno, date) rows with too many missing characteristics.

    Args:
        characteristics (pd.DataFrame): Wide DataFrame indexed by (permno, date),
            columns are characteristics.
        max_missing (float): Maximum allowed fraction of missing values per row
            in [0, 1]. Example: 0.30 keeps rows with up to 30% missing (>=70% coverage)
            and drops rows with more than 30% missing.

    Returns:
        pd.DataFrame: Characteristics with rows exceeding the missingness threshold removed.
    """
    if not (0 <= max_missing <= 1):
        raise ValueError(f"max_missing must be in [0, 1], got {max_missing}")

    # Missing ratio across columns per row
    na_ratio = characteristics.isna().mean(axis=1)

    # Drop rows where missingness exceeds the threshold
    to_drop = na_ratio > max_missing
    n_total = len(to_drop)
    n_drop = int(to_drop.sum())
    pct_drop = (n_drop / n_total * 100) if n_total else 0.0
    print(
        "[missing-filter] Threshold: allow up to "
        f"{max_missing:.0%} missing (i.e., require ≥ {(1 - max_missing):.0%} coverage)."
    )
    print(
        f"[missing-filter] Dropping {n_drop} of {n_total} rows "
        f"({pct_drop:.2f}%) where missing > {max_missing:.0%}."
    )

    kept = characteristics[~to_drop].copy()
    return kept

